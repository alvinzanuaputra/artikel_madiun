                    <p>1. Masa Awal : Kerajaan Majapahit dan Perkembangan Wilayah</p><br>
                    <p>Pada abad ke-14, wilayah yang sekarang dikenal sebagai Kota Madiun merupakan bagian dari wilayah kekuasaan Kerajaan Majapahit. Pada masa itu, Madiun adalah salah satu daerah <br>
                        yang termasuk dalam kawasan yang subur dan strategis. Karena letaknya yang berada di tengah Pulau Jawa, Madiun menjadi titik persinggahan yang penting bagi jalur perdagangan dan <br>
